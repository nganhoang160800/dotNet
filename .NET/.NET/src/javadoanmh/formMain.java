    private void buttonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOKActionPerformed
        // TODO add your handling code here:
        if (textID.getText().equals("")) {

            labelLoiID.setText("Chưa nhập ID!");
            textID.requestFocus();
            // textIDFocusGained);
            return;
        }
        if (new String(textMatKhau.getPassword()).equals("")) {
            labelLoiMatKhau.setText("Chưa nhập mật khẩu!");
            textMatKhau.requestFocus();
            return;
        }
        id = textID.getText();
        BTaiKhoanNV classBTK = new BTaiKhoanNV();
        ResultSet rs = classBTK.getByID(id);
        try {
            if (rs.next()) {
                mk = rs.getString("MatKhauNhanVien").toString();
                if (mk.equals(MD5.getMD5CodeOf(new String(textMatKhau.getPassword())))) {
                    System.out.println("Đăng nhập thành công!");
                    loggedIn = true;
                    inFrameDangNhap.dispose();
                    panelLamViec.setVisible(true);
                    Toolkit t = Toolkit.getDefaultToolkit();
                    Dimension d = t.getScreenSize();
                    this.setSize(d);
                    // this.setLocationByPlatform(true);
                    BNhanVien BND = new BNhanVien("NhanVien");
                    rs = BND.getByID(id);
                    if (rs.next()) {
                        maChucVu = rs.getString("MaChucVu");
                        phanquyenMain();

                        System.out.println("Đăng nhập dưới mã chức vụ: " + maChucVu);
                    }
                } else {
                    labelLoiMatKhau.setText("Mật khẩu không hợp lệ!");
                }
            } else {
                System.out.println("Tài khoản không tồn tại!");
                labelLoiMatKhau.setText("Tài khoản không tồn tại!");
                textIDFocusGained(null);
                textID.requestFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(formMain.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Đăng nhập ngoại lệ: " + ex.getMessage());
        }
    }//GEN-LAST:event_buttonOKActionPerformed

