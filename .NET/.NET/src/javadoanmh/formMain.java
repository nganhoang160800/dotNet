    private void Task(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Task
        // TODO add your handling code here:
        String command = evt.getActionCommand();
        for (int i = 0; i < panelKhungForm.getComponents().length; i++) {
            Component cmpnt = panelKhungForm.getComponent(i);
            if (cmpnt.getName().equals(command)) {
                cmpnt.setVisible(true);
                if (cmpnt.getName().equals("DoiMatKhau")) {
                    informDoiMatKhau.setTextID(id);
                }
                if (cmpnt.getName().equals("DuLieu")) {
                    inFormDuLieu.phanquyen();
                }
                if (cmpnt.getName().equals("PhieuChoThue")) {
                    informPhieuChoThue.setTextID(id);
                }
                if (cmpnt.getName().equals("PhieuBan")) {
                    informPhieuBan.setTextID(id);
                }
            } else {

                cmpnt.setVisible(false);
            }
        }
    }//GEN-LAST:event_Task

