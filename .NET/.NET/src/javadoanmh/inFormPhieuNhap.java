    private void buttonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOKActionPerformed
        // TODO add your handling code here:
        try {
            BPhieuNhap BPN = new BPhieuNhap();

            if (ftextMaPhieuNhap.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không để trống mã phiếu nhập!", "lỗi!", JOptionPane.INFORMATION_MESSAGE);
            } else if (BPN.insertByProperties(Integer.parseInt(ftextMaPhieuNhap.getText()), Integer.parseInt(lsCombNhaCungCap.get(combNhaCungCap.getSelectedIndex())),
                    lsCombNhanVien.get(combTenNhanVien.getSelectedIndex()), lblNgayLapPhieu.getText())) {
                JOptionPane.showMessageDialog(this, "Thêm thành công!", "Đã thêm", JOptionPane.INFORMATION_MESSAGE);
                buttonLamMoiActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(this, "Mã phiếu nhập đã tồn tại!", "Chưa thêm!", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Mã phiếu nhập phải là số!", "Chưa thêm!", JOptionPane.INFORMATION_MESSAGE);
        }
        buttonThem.setEnabled(true);
        buttonOK.setEnabled(false);
        buttonSua.setEnabled(true);
        buttonXoa.setEnabled(false);
    }//GEN-LAST:event_buttonOKActionPerformed

