    private void buttonTruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTruActionPerformed
        // TODO add your handling code here:
        int index = tableChiTietPhieuNhap.getSelectedRow();
        if (index >= 0) {
            DefaultTableModel model = (DefaultTableModel) tableChiTietPhieuNhap.getModel();
            int madia = Integer.parseInt(model.getValueAt(index, 0).toString());
            int soluong = Integer.parseInt(model.getValueAt(index, 2).toString());
            int giatri = Integer.parseInt(spinSoLuong.getValue().toString());
            //System.out.print("-"+soluong);
            int i = soluong - giatri;
            if (i >= 0) {
                //giam so luong di 1
                int index2 = tablePhieuNhap.getSelectedRow();
                if (index2 >= 0) {
                    int sophieunhap = listPhieuNhap.get(index2).getSoPhieuNhap();
                    BPhieuNhap BPN = new BPhieuNhap();
                    if (BPN.updateSoLuongChiTietPhieuNhap(sophieunhap, madia, i)) {
                        //reload
                        BPhieuNhap BPD = new BPhieuNhap();
                        ResultSet rs = BPD.getChiTietPhieuNhap(sophieunhap);
                        displayDataChiTietPhieuNhap(rs);
                        System.out.println("Bớt số lượng thành công!");
                    } else {
                        System.out.print("Bớt số lượng bị lỗi!");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Trừ quá số lượng!", "Không được!", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_buttonTruActionPerformed

